namespace UrlShortener.Web.Models
    {
    public class ShortUrl
        {
        public int Id { get; set; }

        // Почему: храним исходный URL, чтобы при редиректе всегда знать,
        // куда отправлять пользователя. Это основа всей функциональности сервиса.
        public string LongUrl { get; set; } = null!;

        // Почему: короткий код — ключевой идентификатор, по которому происходит
        // поиск при переходе /r/{code}. Он должен быть уникальным и индексированным.
        public string ShortCode { get; set; } = null!;

        // Почему: дата создания нужна для отображения в таблице
        // и для возможной аналитики (например, сортировки или TTL).
        public DateTime CreatedAt { get; set; }

        // Почему: количество переходов — обязательное требование ТЗ.
        // Храним здесь, чтобы не создавать отдельную таблицу логов.
        public int Clicks { get; set; }
        }
    }
